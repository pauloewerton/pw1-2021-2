# fatiamento de listas
L=[1,2,3,4,5]
print(L[0:5]) # posicao 0 ate a 5 (indice alem da ultima)
print(L[:5]) # mesmo que anterior
print(L[:-1]) # posicao 0 ate a ultima (nao inclusiva)
print(L[1:3]) # posicao 1 (inclusiva) ate 3 (nao inclusiva)
print(L[1:4]) # posicao 1 (inclusiva) ate 4 (nao inclusiva)
print(L[3:]) # posicao 3 ate a ultima
print(L[:3]) # posicao 0a ate a 3 (nao inclusiva)
print(L[-1]) # ultimo elemento
print(L[-2]) # penultimo elemento