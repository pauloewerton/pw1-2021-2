# Operador unário de negação
print(not True)
print(not False)

# "E" e "Ou" lógicos - tabelas verdade
print(True and True)
print(True and False)
print(False and True)
print(False and False)

print(True or True)
print(True or False)
print(False or True)
print(False or False)

# Expressões lógicas
salario = 1500
idade = 25
print((salario > 100) and (idade > 18))
